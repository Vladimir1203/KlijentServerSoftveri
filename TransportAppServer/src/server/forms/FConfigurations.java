    private void fillForm() throws FileNotFoundException, IOException {   
        FileInputStream in = new FileInputStream("src\\database\\databaseProp.properties");
        Properties props = new Properties();
        props.load(in);

        String user = props.getProperty("user");
        String password = props.getProperty("password");
        String adress = props.getProperty("address");
        String port = props.getProperty("port");
        String database = props.getProperty("database");
        

        jTextFieldUsername.setText(user);
        jPasswordField1.setText(password);
        jTextFieldHostAdress.setText(adress);
        jTextFieldPortNumber.setText(port);
        jTextFieldDatabase.setText(database);
        
    }
