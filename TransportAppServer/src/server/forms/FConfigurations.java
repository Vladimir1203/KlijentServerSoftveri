    private void jButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveActionPerformed
        String user = jTextFieldUsername.getText();
        String password = String.copyValueOf(jPasswordField1.getPassword());
        String address = jTextFieldHostAdress.getText();
        String port = jTextFieldPortNumber.getText();
        String database = jTextFieldDatabase.getText();
        String url = "jdbc:mysql://"+address+":"+port+"/"+database;

        try {
            FileInputStream in = new FileInputStream("src\\\\database\\\\databaseProp.properties");
            Properties props = new Properties();
            props.load(in);
            in.close();

            FileOutputStream out = new FileOutputStream("db.properties");
            props.setProperty("user", user);
            props.setProperty("port", port);
            props.setProperty("address", address);
            props.setProperty("password", password);
            props.setProperty("url", url);
            props.store(out, "");
            out.close();

            JOptionPane.showMessageDialog(this, "The base is successfully configured.");
            server.setEnabled(true);
            dispose();
        } catch (HeadlessException | IOException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }

    }//GEN-LAST:event_jButtonSaveActionPerformed


